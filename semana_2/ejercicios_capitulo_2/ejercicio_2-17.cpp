// Escribe un programa que imprima los caracteres de V-Z con cada par de caracteres adyacentes separados
// por un espacio ej. VX YZ

#include <iostream>
using namespace std;

int main(){
    // Usando un enunciado con dos operadores de insercion
    cout <<"a) ";
    cout << "VX " << "YZ\n";
    // Usando un enunciado con cuatro operadores de insercion
    cout <<"b) ";
    cout << "VX" << " " << "YZ" <<endl;
    // Usando dos enunciado con un operador de insercion cada uno
    cout <<"c) ";
    cout << "VX ";
    cout << "YZ\n";
    
    return 0;
}